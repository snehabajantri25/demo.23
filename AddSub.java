public class AddSub {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20; 

        int sum = num1 + num2;
        int difference = num1 - num2;

        System.out.println("Addition Result: " + sum);
        System.out.println("Subtraction Result: " + difference);
    }
}